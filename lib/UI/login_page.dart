  onSignUp() async {
    String userId = "";
    try {
      userId = await auth.signUp(
          signupEmailController.text, signupPasswordController.text);
      // print('Signed up user: $userId');
      if (userId.length > 0 && userId != Null) {
        // _showDialog("SignUp successfull");
        // showInSnackBar("SignUp successfull");
        Navigator.of(context).pushReplacement(new MaterialPageRoute(
            builder: (BuildContext context) => new Home()));
      }
    } catch (e) {
      // print('Error: $e');
      // _showDialog("Went's wrong");
      showInSnackBar("Went's wrong");
    }
  }

  onLogIn() async {
    String userId = "";
    String error_message = "";
    try {
      userId = await auth.signIn(
          loginEmailController.text, loginPasswordController.text);
      if (userId.length > 0 && userId != null) {
        // _showDialog("Login successfull!");
        // showInSnackBar("Login successfull!");
        Navigator.of(context).pushReplacement(new MaterialPageRoute(
            builder: (BuildContext context) => new Home()));
      }
    } catch (e) {
      // print('Error: $e');
      error_message = e.toString();
      showInSnackBar("Invalid email or password");
    }
  }


